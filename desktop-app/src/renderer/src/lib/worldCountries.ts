/** Lookup helper — returns `{ flag, label }` for a code, or `null` if unknown. */
export function worldCountryByCode(code: string): WorldCountry | null {
  return WORLD_COUNTRIES.find((c) => c.code === code) ?? null
}

/** Display string for a country code (flag + label, or just the raw code). */
export function worldCountryLabel(code: string): string {
  const c = worldCountryByCode(code)
  return c ? `${c.flag} ${c.label}` : code
}

/**
 * Filter helper for picker UIs — case-insensitive substring match on label
 * or code. Returns the filtered slice; pass `[]` to clear.
 */
export function searchWorldCountries(query: string): ReadonlyArray<WorldCountry> {
  const q = query.trim().toLowerCase()
  if (!q) return WORLD_COUNTRIES
  return WORLD_COUNTRIES.filter(
    (c) => c.label.toLowerCase().includes(q) || c.code.toLowerCase().includes(q)
  )
}
/**
 * Comprehensive ISO 3166-1 alpha-2 country list for the employee roster.
 *
 * Used by the employee form + list — `Employee.country` is intentionally
 * open-ended (the employer may pay people anywhere in the world) so we
 * ship the full list here rather than restricting to the 4-jurisdiction
 * `COUNTRIES` preset used for the company profile.
 *
 * The list is alphabetised by `label` for the picker (search by typing).
 * Flags are Unicode regional-indicator pairs — they render on every
 * modern OS, no font dependency.
 *
 * NOTE: `Employee.country` is `string` in the schema (not `CountryCode`),
 * so we tolerate rows whose country isn't in this table (e.g. legacy data,
 * Kosovo before ISO adoption). Lookup falls back to the raw code.
 */
export interface WorldCountry {
  /** ISO 3166-1 alpha-2 (uppercase), e.g. "JP", "US", "GB". */
  code: string
  /** English short name (UN/CEFAT style). */
  label: string
  /** Unicode flag emoji (regional-indicator pair). */
  flag: string
}

